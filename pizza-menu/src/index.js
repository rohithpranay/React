function Footer() {
  const hour = new Date().getHours();
  const openHour = 12;
  const closedHour = 22;
  const isOpen = hour >= openHour && hour <= closedHour;

  return (
    <footer className="footer">
      {isOpen ? (
        <Order hour={openHour} />
      ) : (
        <p>
          We're happy to welxcome between {openHour}:00 and {closedHour}:00
        </p>
      )}
    </footer>
  );
  // return React.createElement("footer", null, "We're Currently Open! ");
}

function Order(props) {
  return (
    <div className="order">
      <p>We're Open Untill {props.hour}:00.Come Visit Us or Order Online</p>
      <button className="btn">Order</button>
    </div>
  );
}

